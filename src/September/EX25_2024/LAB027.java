package September.EX25_2024;

public class LAB027 {
    public static void main(String[] args) {
// Loops - Foe, while, do while - a task performed multiple times
/*        FOR LOOP
        FOR key word | for (A:B:C)
        A - Initialization | B - Condition for termination | C - Increment & Decrement
 */
        for (int i = 1; i<=10; i++){
            System.out.println(i); // value will keeps on printing till its less than 10
        }

        for (int pai = 1; pai<6; pai++){
            System.out.println(pai);
        }

        for (int a = 0; a > 1; a++){
            System.out.println(a);
        }
    }
}
